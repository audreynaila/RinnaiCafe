    private void buttonCostum1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCostum1ActionPerformed
try {
    // Koneksi ke database
    Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_caferinai", "root", "");

    // Mengambil total harga dari tabel orders_items
    String selectSql = "SELECT SUM(Price) AS TotalHarga FROM orders_items";
    PreparedStatement selectStmt = (PreparedStatement) conn.prepareStatement(selectSql);
    ResultSet resultSet = selectStmt.executeQuery();

    if (resultSet.next()) {
        int totalHarga = resultSet.getInt("TotalHarga");

        // Menampilkan hasil ke JTextField total1
        total1.setText(Integer.toString(totalHarga));
        total.setText(Integer.toString(totalHarga));
        
    }

    // Menutup koneksi
    resultSet.close();
    selectStmt.close();
    conn.close();
} catch (SQLException ex) {
    ex.printStackTrace();
}

    }//GEN-LAST:event_buttonCostum1ActionPerformed

