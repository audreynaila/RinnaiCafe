    private void showTableData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ProductID");
        model.addColumn("Name");
        model.addColumn("Price");
        model.addColumn("Stock");

        // Melakukan koneksi ke database dan mengambil data dari tabel orders_item
        Connection conn = null;
        Statement stmt = null;
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_caferinai", "root", "");
            stmt = conn.createStatement();

            String sql = "SELECT ProductID, Name, Price, Stock FROM orders_items";
            ResultSet rs = stmt.executeQuery(sql);

            // Masukkan data dari ResultSet ke dalam model tabel
            while (rs.next()) {
                String productID = rs.getString("ProductID");
                String name = rs.getString("Name");
                int price = rs.getInt("Price");
                int stock = rs.getInt("Stock");
                model.addRow(new Object[]{productID, name, price, stock});
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        // Set model tabel dengan data dari database
        JTable.setModel(model);
    }

