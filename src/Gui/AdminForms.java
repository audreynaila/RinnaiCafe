    private void btnUpdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdate1ActionPerformed
        if (selectedRow != -1) {
            // Perbarui data pada baris yang dipilih dengan nilai dari input pengguna
            String productID = table.getValueAt(selectedRow, 0).toString();

            // Tampilkan dialog input untuk memperbarui nilai
            String name = JOptionPane.showInputDialog(this, "Enter updated name:");
            String priceStr = JOptionPane.showInputDialog(this, "Enter updated price:");
            String stock = JOptionPane.showInputDialog(this, "Enter updated stock:");

            // Lakukan tindakan perbarui di sini, misalnya melakukan update pada database dengan nilai-nilai ini
            String query = "UPDATE orders_items SET Name = ?, Price = ?, Stok = ? WHERE ProductID = ?";

            try {
                Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/db_cafe", "username", "password");
                PreparedStatement statement = connection.prepareStatement(query);
                statement.setString(1, name);
                statement.setInt(2, Integer.parseInt(priceStr));
                statement.setString(3, stock);
                statement.setString(4, productID);

                // Jalankan pernyataan update
                int rowsUpdated = statement.executeUpdate();

                if (rowsUpdated > 0) {
                    // Perbarui nilai pada tabel
                    table.setValueAt(name, selectedRow, 1);
                    table.setValueAt(priceStr, selectedRow, 2);
                    table.setValueAt(stock, selectedRow, 3);

                    // Tampilkan pesan sukses
                    JOptionPane.showMessageDialog(this, "Data updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to update data.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Failed to update data.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No row selected.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdate1ActionPerformed

    private void btnCreate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreate1ActionPerformed
        // Retrieve the input values from the user
        String name = JOptionPane.showInputDialog(this, "Enter name:");
        String priceStr = JOptionPane.showInputDialog(this, "Enter price:");
        String stock = JOptionPane.showInputDialog(this, "Enter stock:");

        // Create a new product in the database
        String query = "INSERT INTO orders_items (Name, Price, Stok) VALUES (?, ?, ?)";

        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/db_cafe", "username", "password");
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, name);
            statement.setInt(2, Integer.parseInt(priceStr));
            statement.setString(3, stock);

            // Execute the insert statement
            int rowsInserted = statement.executeUpdate();

            if (rowsInserted > 0) {
                // Retrieve the generated ProductID for the new product
                ResultSet generatedKeys = statement.getGeneratedKeys();
                if (generatedKeys.next()) {
                    int productId = generatedKeys.getInt(1);

                    // Create an array to hold the data of the new product
                    Object[] newRowData = {productId, name, priceStr, stock};

                    // Add the new product data to the table model
                    DefaultTableModel model = (DefaultTableModel) table.getModel();
                    model.addRow(newRowData);

                    // Tampilkan pesan sukses
                    JOptionPane.showMessageDialog(this, "Product created successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed to create product.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to create product.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCreate1ActionPerformed

