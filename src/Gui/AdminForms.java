    private void showTableData() {
        // Set table column names
        tableModel.setColumnIdentifiers(new String[]{"Product ID", "Name", "Price", "Stock"});

        try {
            // Establish a connection to the database
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_caferinai", "root", "");

            // Create a statement
            Statement statement = conn.createStatement();

            // Execute the query to retrieve data from the table
            ResultSet resultSet = statement.executeQuery("SELECT * FROM orders_items");

            // Loop through the result set and add rows to the table model
            while (resultSet.next()) {
                String productID = resultSet.getString("ProductID");
                String name = resultSet.getString("Name");
                int price = resultSet.getInt("Price");
                String stock = resultSet.getString("Stock");

                tableModel.addRow(new Object[]{productID, name, price, stock});
            }

            // Close the connection, statement, and result set
            resultSet.close();
            statement.close();
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
            // Show error message
            JOptionPane.showMessageDialog(this, "Error retrieving data.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

  
